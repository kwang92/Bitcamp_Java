
public class Operator {
	public static void main(String[] args) {
		/*
		 * 연산자 - [ 산술연산, 비교연산, 논리연산, 증감연산, 대입연산, 비트연산 ]
		 *   산술연산 : +, -, *, /, %
		 *   비교연산 : >, <, =>, <=, ==
		 *   
		 */
		
		System.out.println(10+6);
		System.out.println(10-6);
		System.out.println(10*6);
		System.out.println(10/6);	// 정수끼리의 연산은 정수, 실수 나누기 정수연산은 정수가 실수로 변환
		System.out.println(10%6);	// 나눈 나머지
		
	}
}