	// 실수형 상수 표현하는 방법
public class Literal2 {
	
	public static void main(String[] args) {
		double r1 = 123.5;
		float r2 = (float)123.5;
		float r3 = 123.5f;
		System.out.println(r1 + r2);
		
		// Java에서의 지수표기법을 이용한 실수 표현
		float jf = 1.235e2f;
		double jd = 1.235e-2;
		
		char c = '굿';
		boolean isTrue = false;
		
		System.out.println(""+c+isTrue);
		
		
	}
}
