
public class Multiply_Program {	// Multiply 클래스
	public static void main(String[] args) {	// main 메서드
		int num1 = 17, num2 = 17, mul;	
		// 정수형 변수 num1, num2값 초기화 및 mul 선언
		
		mul = num1 * num2;	// num1와 num2의 곱의 결과를 mul에 넣어줌
		
		System.out.println("두 수의 곱은 " + mul + "입니다.");	
		// num1, num2의 곱 결과를 콘솔에 출력
	}
}
